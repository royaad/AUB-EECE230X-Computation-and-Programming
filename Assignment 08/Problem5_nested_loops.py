def distance(pt_1, pt_2):
    return ((pt_1[0]-pt_2[0])**2 + (pt_1[1]-pt_2[1])**2)**0.5

def ClosestPairOfPoints(L):
    n = len(L)
    Dist = float('inf')
    for i in range(n):
        for j in range(i+1,n):
            dist = distance(L[i], L[j])
            if dist < Dist:
                Dist = dist
                pt_1 = L[i]
                pt_2 = L[j]
    return (pt_1, pt_2, Dist)

